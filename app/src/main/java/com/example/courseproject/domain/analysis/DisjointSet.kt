package com.example.courseproject.domain.analysis

/**
 * Система непересекающихся множеств (Union-Find) со сжатием путей
 * и объединением по рангу. Применяется для поиска связных компонент
 * графа велосипедной инфраструктуры при расчёте критерия непрерывности.
 *
 * Идея структуры. Каждое множество представлено деревом, корень которого —
 * это «представитель» (root). Узел хранит ссылку на своего родителя; у корня
 * родитель — он сам. Операция find() поднимается до корня; union() объединяет
 * два множества, делая корень одного потомком корня другого.
 *
 * Оптимизации:
 *   – Сжатие путей (path compression) в find() — при подъёме перевешиваем
 *     все встретившиеся узлы прямо на корень. Это делает следующие операции
 *     find для этих узлов почти константными.
 *   – Объединение по рангу (union by rank) в union() — дерево меньшей высоты
 *     становится потомком дерева большей высоты. Это не даёт деревьям расти
 *     в линейные «цепочки» и тоже ускоряет работу.
 *
 * Амортизированная сложность операций — практически O(α(N)), где α —
 * обратная функция Аккермана; для всех разумных N её значение не превышает 4.
 */
class DisjointSet {

    // Map «узел → родитель». У корня parent[x] == x.
    private val parent = HashMap<Long, Long>()
    // Map «узел → ранг» (верхняя оценка высоты дерева, нужна только для корней).
    private val rank = HashMap<Long, Int>()

    /** Добавляет элемент в структуру как самостоятельное множество (если ещё не добавлен). */
    fun add(element: Long) {
        // Если элемент впервые увиден — создаём для него отдельное множество…
        if (element !in parent) {
            parent[element] = element   // …родитель указывает на самого себя (узел — корень)…
            rank[element] = 0           // …и ранг 0 — это дерево из одного узла.
        }
    }

    /** Возвращает представителя множества, которому принадлежит элемент. */
    fun find(element: Long): Long {
        // Если элемент новый — добавляем его как одиночное множество.
        add(element)
        // [1] Поднимаемся по родительским ссылкам, пока не упрёмся в корень
        //     (узел, у которого parent[x] == x).
        var root = element
        while (parent[root] != root) {
            root = parent.getValue(root)
        }
        // [2] Сжатие путей: проходим этот же путь ещё раз и перевешиваем
        //     каждый встретившийся узел напрямую на корень. После этого
        //     повторные вызовы find() для этих узлов отработают за O(1).
        var current = element
        while (parent[current] != root) {
            val next = parent.getValue(current)   // запоминаем, куда идти дальше
            parent[current] = root                // текущий узел теперь указывает прямо на корень
            current = next                        // переходим к следующему звену цепи
        }
        return root
    }

    /** Объединяет множества, содержащие два элемента. */
    fun union(a: Long, b: Long) {
        // Находим корни двух множеств.
        val rootA = find(a)
        val rootB = find(b)
        // Уже в одном множестве — объединять нечего.
        if (rootA == rootB) return
        // Сравниваем ранги корней и подвешиваем меньшее дерево под большее
        // (это и есть «объединение по рангу» — не даёт высотам расти быстрее логарифма).
        val rankA = rank.getValue(rootA)
        val rankB = rank.getValue(rootB)
        when {
            rankA < rankB -> parent[rootA] = rootB     // A ниже — подвешиваем под B
            rankA > rankB -> parent[rootB] = rootA     // B ниже — подвешиваем под A
            else -> {
                // Ранги равны: подвешиваем B под A и увеличиваем ранг A на 1
                // (высота нового дерева могла вырасти на единицу).
                parent[rootB] = rootA
                rank[rootA] = rankA + 1
            }
        }
    }

    /** Проверяет, принадлежат ли два элемента одному множеству. */
    fun connected(a: Long, b: Long): Boolean = find(a) == find(b)
}
