package com.example.courseproject.domain.model

/**
 * Прямоугольная область анализа в географических координатах (WGS84).
 * Используется как входной параметр для загрузки данных OSM и расчёта оценки.
 *
 * Углы задаются нижней и верхней широтой (south/north) и западной/восточной
 * долготой (west/east) — это стандартное представление bounding box в OSM.
 */
data class BoundingBox(
    val south: Double,    // минимальная широта (нижняя граница)
    val west: Double,     // минимальная долгота (левая граница)
    val north: Double,    // максимальная широта (верхняя граница)
    val east: Double,     // максимальная долгота (правая граница)
) {
    /** Широта центра области — среднее между нижней и верхней границей. */
    val centerLat: Double get() = (south + north) / 2.0

    /** Долгота центра области — среднее между левой и правой границей. */
    val centerLon: Double get() = (west + east) / 2.0

    /**
     * Стабильный ключ области для кэширования.
     * Координаты округляются до 5 знаков после запятой (~1 м на местности),
     * чтобы при незначительных сдвигах карты кэш не промахивался лишний раз.
     */
    fun cacheKey(): String {
        // Внутренняя функция: число вида 55.12345 → строка «5512345» (×100 000).
        fun part(value: Double): String = (value * 1e5).toLong().toString()
        // Собираем ключ из четырёх частей, разделённых подчёркиванием.
        return "${part(south)}_${part(west)}_${part(north)}_${part(east)}"
    }
}
